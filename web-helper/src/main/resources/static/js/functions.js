$(document).ready(function () {
    var clicks = 0;

    $('span#footer-down').on('click', function () {
        $('footer').animate({opacity: 0, bottom: '0%'}, 200, function(){
            $(this).css('height', '1em');
            $(this).css('font-size', '0.8em');

            clicks++;

            if (clicks >= 2) {
                $(this).css('display', 'none');
            }

            $('footer').animate({opacity: 1}, 200);
        });
    });
});

